import matplotlib.pyplot as plt
import numpy as np

plt.figure(figsize=(10,4), dpi=100)
plt.figure(1)
ax1 = plt.subplot(121)
openmx = np.loadtxt('Im_chi_11.dat')
ax1.plot(openmx[:,0], openmx[:,1], label='openmx')
abinit = np.loadtxt('optic_2_0001_0001-linopt_9100.out')
ax1.plot(abinit[:118,0], abinit[:118,1], label='abinit')
plt.xlabel('$\omega$ (eV)')
plt.ylabel('Im $\epsilon$${_r}$$^{xx}$')
ax1.legend()
plt.xlim(0,6)
plt.title('1L-GeS Im_chi_11')
ax2 = plt.subplot(122)
openmx = np.loadtxt('Re_chi_11.dat')
ax2.plot(openmx[:,0], openmx[:,1], label='openmx')
abinit = np.loadtxt('optic_2_0001_0001-linopt_9100.out')
ax2.plot(abinit[119:237,0], abinit[119:237,1], label='abinit')
plt.xlabel('$\omega$ (eV)')
plt.ylabel('Re $\epsilon$${_r}$$^{xx}$')
ax2.legend()
plt.xlim(0,6)
plt.title('1L-GeS Re_chi_11')
ax2.legend()
plt.tight_layout()
plt.savefig('Chi_11.png')
